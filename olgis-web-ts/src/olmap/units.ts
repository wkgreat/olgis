export const meter2kilometer = (meter:number) => meter / 1000;
export const kilometer2meter = (kilometer:number) => kilometer * 1000;
export const meter2imperial = (meter:number) => meter * 0.000621371192237;
export const imperial2meter = (imperial:number) => imperial / 0.000621371192237;
export const meter2nautical = (meter:number) => meter * 0.000539956803456;
export const nautical2meter = (nautical:number) => nautical / 0.000539956803456;
export const degree2Radian = (degree:number) => degree * Math.PI / 180;
export const radian2Degree = (radian: number) => radian * 180 / Math.PI;